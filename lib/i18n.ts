export function tr(language: Language, key: string, vars?: Record<string, string | number>): string {
  const dictionary = dictionaries[language] ?? dictionaries.hu
  let value =
    (dictionary[key as keyof typeof dictionary] as string) ??
    (dictionaries.en[key as keyof typeof dictionaries.en] as string) ??
    key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      value = value.replace(`{${k}}`, String(v))
    }
  }
  return value
}

export function getLanguage(value?: string): Language {
  if (value === 'fr') return 'fr'
  if (value === 'en') return 'en'
  return 'hu'
}
